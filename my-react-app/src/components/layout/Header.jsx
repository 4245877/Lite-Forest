import React, { useState, useEffect, useRef } from 'react';
import { Link } from 'react-router-dom';
import './Header.css';

const Header = () => {
  const [isMobileMenuOpen, setIsMobileMenuOpen] = useState(false);
  const firstNavLinkRef = useRef(null);

  const toggleMobileMenu = () => setIsMobileMenuOpen(prev => !prev);

  // –ë–ª–æ–∫–∏—Ä–æ–≤–∫–∞ –ø—Ä–æ–∫—Ä—É—Ç–∫–∏ –∏ —Ñ–æ–∫—É—Å –ø—Ä–∏ –æ—Ç–∫—Ä—ã—Ç–∏–∏
  useEffect(() => {
    if (isMobileMenuOpen) {
      document.body.classList.add('no-scroll');
      setTimeout(() => firstNavLinkRef.current?.focus(), 120);
    } else {
      document.body.classList.remove('no-scroll');
    }
    return () => document.body.classList.remove('no-scroll');
  }, [isMobileMenuOpen]);

  // –ó–∞–∫—Ä—ã—Ç—å –º–æ–±–∏–ª—å–Ω–æ–µ –º–µ–Ω—é –ø—Ä–∏ –ø–µ—Ä–µ—Ö–æ–¥–µ –Ω–∞ –¥–µ—Å–∫—Ç–æ–ø
  useEffect(() => {
    const mql = window.matchMedia('(min-width: 769px)');
    const onChange = e => { if (e.matches) setIsMobileMenuOpen(false); };
    if (mql.addEventListener) mql.addEventListener('change', onChange);
    else mql.addListener(onChange);
    return () => {
      if (mql.removeEventListener) mql.removeEventListener('change', onChange);
      else mql.removeListener(onChange);
    };
  }, []);

  // –ó–∞–∫—Ä—ã—Ç—å –º–µ–Ω—é –ø–æ Escape
  useEffect(() => {
    const onKeyDown = e => { if (e.key === 'Escape' && isMobileMenuOpen) setIsMobileMenuOpen(false); };
    window.addEventListener('keydown', onKeyDown);
    return () => window.removeEventListener('keydown', onKeyDown);
  }, [isMobileMenuOpen]);

  const handleNavLinkClick = () => { if (isMobileMenuOpen) setIsMobileMenuOpen(false); };

  return (
    <header className="app-header">
      <div className="header-container">
        {/* –õ–æ–≥–æ—Ç–∏–ø */}
        <Link to="/" className="logo">
          <div className="logo-icon">üåø</div>
          <span className="logo-text">
            Lite<span className="logo-accent">Forest</span>
          </span>
        </Link>

        {/* –û—Å–Ω–æ–≤–Ω–æ–µ –º–µ–Ω—é */}
        <nav
          id="main-nav"
          className={`main-nav ${isMobileMenuOpen ? 'mobile-open' : ''}`}
          role="navigation"
          aria-label="–û—Å–Ω–æ–≤–Ω–æ–µ –º–µ–Ω—é"
        >
          <Link to="/" className="nav-link" onClick={handleNavLinkClick} ref={firstNavLinkRef}>
            <span>–ì–æ–ª–æ–≤–Ω–∞</span>
          </Link>
          <Link to="/catalog" className="nav-link" onClick={handleNavLinkClick}>
            <span>–ö–∞—Ç–∞–ª–æ–≥</span>
          </Link>
          <Link to="/promotions" className="nav-link" onClick={handleNavLinkClick}>
            <span>–ê–∫—Ü—ñ—ó</span>
          </Link>
          <Link to="/about" className="nav-link" onClick={handleNavLinkClick}>
            <span>–ü—Ä–æ –Ω–∞—Å</span>
          </Link>
          <Link to="/contacts" className="nav-link" onClick={handleNavLinkClick}>
            <span>–ö–æ–Ω—Ç–∞–∫—Ç–∏</span>
          </Link>
        </nav>

        {/* –î–µ–π—Å—Ç–≤–∏—è */}
        <div className="header-actions">
          <button className="action-btn search-btn" aria-label="–ü–æ–∏—Å–∫">
            <svg
              className="icon-search"
              viewBox="0 0 24 24"
              fill="none"
              stroke="currentColor"
              aria-hidden="true"
              focusable="false"
              role="img"
            >
              <circle cx="11" cy="11" r="8" />
              <path d="M21 21L16.65 16.65" />
            </svg>
          </button>

          <Link to="/cart" className="action-btn cart-btn" aria-label="–ö–æ—Ä–∑–∏–Ω–∞">
            <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor">
              <path d="M3 3h2l.4 2M7 13h10l4-8H5.4M7 13L5.4 5M7 13l-2.293 2.293c-.63.63-.184 1.707.707 1.707H17M17 13v6a2 2 0 0 1-2 2H9a2 2 0 0 1-2-2v-6" />
            </svg>
            <span className="cart-count">0</span>
          </Link>

          <Link to="/login" className="action-btn profile-btn" aria-label="–õ–∏—á–Ω—ã–π –∫–∞–±–∏–Ω–µ—Ç">
            <svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor">
              <path d="M20 21v-2a4 4 0 0 0-4-4H8a4 4 0 0 0-4 4v2" />
              <circle cx="12" cy="7" r="4" />
            </svg>
          </Link>

          {/* –ú–æ–±–∏–ª—å–Ω–æ–µ –º–µ–Ω—é –∫–Ω–æ–ø–∫–∞ */}
          <button
            className={`mobile-menu-btn ${isMobileMenuOpen ? 'active' : ''}`}
            onClick={toggleMobileMenu}
            aria-label="–ú–µ–Ω—é"
            aria-expanded={isMobileMenuOpen}
            aria-controls="main-nav"
          >
            <span></span>
            <span></span>
            <span></span>
          </button>
        </div>
      </div>

      {/* –ú–æ–±–∏–ª—å–Ω–æ–µ –º–µ–Ω—é overlay */}
      {isMobileMenuOpen && (
        <div className="mobile-menu-overlay" onClick={toggleMobileMenu} aria-hidden="true"></div>
      )}
    </header>
  );
};

export default Header;
